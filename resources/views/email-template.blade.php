                          <p style="font-size:12px; line-height: 15px;text-align:justify;color:#5d5f61; margin-top: 15px;">
                            <b>New Zealand Minimum Data Sheet</b><br>
                            <b>BOTOX<sup>®</sup> (botulinum toxin type A) purifed neurotoxin complex</b> is a prescription medicine containing 100 units
                            of botulinum toxin type A for injection. <b><i>Indications:</i></b> For the treatment of overactive bladder with symptoms of
                            urinary incontinence, urgency, and frequency, in adults who have an inadequate response to or are intolerant of an
                            anticholinergic medication; urinary incontinence due to neurogenic detrusor overactivity (eg. spinal cord injury or
                            multiple sclerosis); in adults who have an inadequate response to or are intolerant of an anticholinergic medication;
                            prophylaxis of headaches in adults with chronic migraine (headaches on at least 15 days per month with headaches
                            lasting 4 hours a day or longer, of which 8 days are with migraine); strabismus; blepharospasm associated with
                            dystonia, including benign essential blepharospasm & VIIth nerve disorders; cervical dystonia (spasmodic torticollis);
                            focal spasticity in children 2 years & older; primary hyperhidrosis of the axillae; focal spasticity in adults; upper facial
                            rhytides (forehead, crow’s feet & glabellar lines). <b><i>Contraindications:</i></b> Hypersensitivity to ingredients; myasthenia
                            gravis or Eaton Lambert Syndrome; infection at injection site(s); intradetrusor injection is contraindicated in patients
                            who are not willing and/or able to initiate catheterisation post-treatment, if required. <b><i>Precautions:</i></b> Botulinum toxin
                            effects may be observed beyond site of local injection with symptoms consistent with mechanism of action and
                            reported hours to weeks after injection. Symptoms may include muscular weakness, ptosis, diplopia, blurred vision,
                            facial weakness, swallowing and speech disorders, constipation, aspiration pneumonia, diffculty breathing and
                            respiratory depression. Risk of symptoms is greatest in children with spasticity, but can also occur in adults
                            particularly those on high doses. Swallowing/ breathing diffculties can be life threatening and there have been
                            reports of death (relationship to BOTOX<sup>®</sup> not established). Serious adverse events including fatal outcomes have
                            been reported in patients who had received BOTOX<sup>®</sup> injected directly into salivary glands, the oro-lingual-pharyngeal
                            region, oesophagus and stomach. Use with aminoglycosides or drugs that interfere with neuromuscular transmission;
                            peripheral motor neuropathic diseases or neuromuscular junctional disorders; inﬂammation at injection sites;
                            excessive weakness in target muscle; pregnancy & lactation. Generalised weakness & myalgia may be related to
                            systemic absorption. Different botulinum preparations are not therapeutically equivalent. Exercise extreme caution
                            should substitution with another botulinum preparation be necessary. <span style="text-decoration: underline;">Bladder dysfunction:</span> Catheterisation may be
                            required in patients experiencing diffculty in voiding post treatment. <span style="text-decoration: underline;">Neurogenic Detrusor Overactivity:</span> Autonomic
                            dysreﬂexia associated with performing the cystoscopy. <span style="text-decoration: underline;">Blepharospasm:</span> Reduced blinking following injection of the
                            orbicularis muscle can lead to corneal pathology. Caution with patients at risk of angle closure glaucoma, including
                            anatomically narrow angles. <span style="text-decoration: underline;">Strabismus:</span> Inducing paralysis in extraocular muscles may produce spatial
                            disorientation, double vision or past pointing. Use in chronic paralytic strabismus only in conjunction with surgical
                            repair to reduce antagonist contracture. <span style="text-decoration: underline;">Adult Focal Spasticity:</span> Not likely to be effective at a joint affected by a
                            known fxed contracture. Not for treatment of focal lower limb spasticity in adult post-stroke patients if muscle tone
                            reduction is not expected to result in improved function, improved symptoms or to facilitate care or such patients at
                            risk of fall. <span style="text-decoration: underline;">Cervical Dystonia (spasmodic torticollis):</span> Possibility of dysphagia, with occasional dyspnoea. May be
                            decreased by limiting dose injected into the sternocleidomastoid muscle to &#60;100U. <span style="text-decoration: underline;">Primary Hyperhidrosis of the
                            Axillae:</span> Consider causes of secondary hyperhidrosis to avoid symptomatic treatment. Avoid treatment in pregnancy,
                            lactation & elective surgery requiring general anaesthesia. <span style="text-decoration: underline;">Upper facial rhytides:</span> Caution in patients with marked
                            facial asymmetry, ptosis, excessive dermatochalasis, deep dermal scarring, thick sebaceous skin or the inability to
                            substantially lessen glabellar lines by physically spreading them apart. <span style="text-decoration: underline;">Juvenile Use:</span> Safety & effectiveness below
                            12 years not established for blepharospasm, strabismus, VIIth nerve disorder, cervical dystonia, hyperhidrosis or
                            upper facial rhytides. Safety and effectiveness below 2 years not established for focal spasticity. Caution should be
                            exercised when treating patients with signifcant disability & co-morbidities and elderly. Caution should be exercised
                            after treatment of BOTOX<sup>®</sup> as it can have an effect on the ability to drive and use machines. <b><i>Adverse Reactions:</i></b>
                            Usually transient & occur within frst week of injection. ≥1% Localised pain; tenderness; bruising; local & general
                            weakness; erythema; oedema; ptosis; irritation/tearing; vertical deviation; blurring of vision; facial droop; pain;
                            dysphagia; soreness; malaise; upper respiratory infection; nausea; headache; drowsiness; stiffness; dry mouth;
                            dizziness; rhinitis; hypertonia; numbness; diplopia; lethargy; fever; ﬂu syndrome; trigger fnger; clumsiness; falling;
                            hypokinesia; increased frequency of micturition; joint dislocation; muscle spasms; convulsions; nasopharyngitis;
                            pneumonia; vomiting; contusion; leg pain; leg cramps; knee pain; ankle pain; arm pain; hypertonia; accidental
                            injury; incoordination; paraesthesia; asthenia; hyperkinesia; increase in non-axillary sweating; vasodilation;
                            blepharoptosis; face pain; skin tightness; temporary lateral lower eyelid droop; eyebrow ptosis; eyelid swelling;
                            aching/itching forehead; feeling of tension; seizures, migraine, facial paresis, eyelid ptosis, pruritus, rash, neck
                            pain, musculoskeletal stiffness, muscular weakness, myalgia, musculoskeletal pain, muscle tightness, injection site
                            pain, urinary tract infection, urinary retention, fatigue, insomnia, constipation, gait disturbance, bladder diverticulum,
                            haematuria, dysuria, autonomic dysreﬂexia bacteriuria, pollakiuria, residual urine volume, lagophthalmos, dysphonia,
                            dry eye, peripheral oedema, eyelid oedema, arthralgia and localised muscle twitching/involuntary muscle contraction.
                            <b><i>Dose/Administration:</i></b> Use one vial for one patient. Store reconstituted BOTOX<sup>®</sup> in refrigerator; use within 24
                            hours. <span style="text-decoration: underline;">Overactive bladder:</span> 100U injected into the detrusor muscle via a ﬂexible or rigid cystoscope, avoiding the
                            trigone. <span style="text-decoration: underline;">Neurogenic Detrusor Overactivity:</span> 200U injected into the detrusor muscle via a ﬂexible or rigid cystoscope,
                            avoiding the trigone. <span style="text-decoration: underline;">Chronic migraine:</span> 155U to 195U administered intramuscularly (IM) divided across 7 specifc
                            head/neck muscle areas. <span style="text-decoration: underline;">Blepharospasm:</span> Initially 1.25U to 2.5U injected into upper lid medial & lateral pre-tarsal
                            orbicularis oculi, & into lower lid lateral pre-tarsal orbicularis oculi. Cumulative dose over 2 months should not
                            exceed 200U. <span style="text-decoration: underline;">Strabismus:</span> Initial doses 1.25–2.5U to 2.5–5.0U per muscle. Maximum single injection for any one
                            muscle is 25U. <span style="text-decoration: underline;">VIIth Nerve Disorders:</span> Dosing as for unilateral blepharospasm. Inject other facial muscles as needed.
                            <span style="text-decoration: underline;">Focal Spasticity in Children 2 Years & Older:</span> 0.5-2.0U/kg body weight for upper limb & 2.0-4.0U/kg body weight for
                            lower limb. 4U/kg or 200U (the lesser amount) for equinus foot deformity. Other muscles range 3.0-8.0U/kg body
                            weight & do not exceed 300U divided among muscles at any treatment session. <span style="text-decoration: underline;">Focal Spasticity in Adults:</span>
                            Individualise dosing. <span style="text-decoration: underline;">Cervical Dystonia (spasmodic torticollis):</span> Individualise dosing. Maximum dose 360U over
                            2 months. <span style="text-decoration: underline;">Primary Hyperhidrosis of the Axillae:</span> 50U intradermally to each axilla in multiple sites 1-2 cm apart.
                            Glabellar Lines: 2x4U in each corrugator muscle & 4U in the procerus muscle for a total dose of 20U. <span style="text-decoration: underline;">Crow’s Feet:</span>
                            2-6U/injection site, 3 sites bilaterally in lateral orbicularis oculi. <span style="text-decoration: underline;">Forehead Lines:</span> 2-6U/injection site, 4 sites in frontalis
                            muscle. <b>Last Amended Date:</b> March 2020, DS v14.
